def calculate(operator, first_input, second_input)


  if operator == "+"
    result = (first_input + second_input)
  elsif operator == "-"
    result = (first_input - second_input)
  elsif operator == "*"
    result = (first_input * second_input)
  elsif operator == "/"
    result = (first_input / second_input)
  elsif operator == "/"
    result = (first_input / second_input)
  elsif operator == "%"
    result = (first_input % second_input)
  else
    result = ""
  end
return result

end


